-- TRIGGER: MAXIMO.ALLWEATHERPRODUCT_T
CREATE OR REPLACE TRIGGER MAXIMO.ALLWEATHERPRODUCT_T
BEFORE INSERT OR UPDATE ON MAXIMO.ALLWEATHERPRODUCTS
REFERENCING NEW AS NEW_ROW OLD AS OLD_ROW
FOR EACH ROW
BEGIN
    DECLARE NEXTVAL INTEGER;
    SELECT NEXT VALUE FOR MAXIMO.MAXSEQ INTO NEXTVAL FROM SYSIBM.SYSDUMMY1;
    SET NEW_ROW.ROWSTAMP = NEXTVAL;
END;
